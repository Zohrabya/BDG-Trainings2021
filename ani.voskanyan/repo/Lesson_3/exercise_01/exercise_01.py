#a. Using one statement with one print function
line_a = "*" * 19 + "\n" 
print(line_a * 4)

#b. Using four statement with one print function
line_b_1 = "*" * 19
line_b_2 = "*" * 19
line_b_3 = "*" * 19
line_b_4 = "*" * 19
print(f'''{line_b_1} 
{line_b_2}
{line_b_3}
{line_b_4}''')

#c. Using four statement with four print function
line_c_1 = "*" * 19
line_c_2 = "*" * 19
line_c_3 = "*" * 19
line_c_4 = "*" * 19
print(line_c_1)
print(line_c_2)
print(line_c_3)
print(line_c_4)
