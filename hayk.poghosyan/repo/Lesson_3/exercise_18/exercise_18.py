pi = 3.14159
circle_radius = int(input("Enter a radius: "))
diameter = 2 * circle_radius
circumference = 2 * pi * circle_radius
area = 2 * pi * circle_radius * circle_radius
print("Diameter is: " + str(diameter) + "\n" + "Circumference is: " + str(circumference) + "\n" + "Area is: " + str(area))